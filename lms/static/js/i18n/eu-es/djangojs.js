    "MONTH_DAY_FORMAT": "F\\r\\e\\n j\\a",
    "NUMBER_GROUPING": 3,
    "SHORT_DATETIME_FORMAT": "Y-m-d H:i",
    "SHORT_DATE_FORMAT": "Y-m-d",
